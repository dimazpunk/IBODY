  function showTime() {
    var a_p = "";
    var today = new Date();
    var curr_hour = today.getHours();
    var curr_minute = today.getMinutes();
    var curr_second = today.getSeconds();
    if (curr_hour < 12) {
        a_p = "AM";
    } else {
        a_p = "PM";
    }
    if (curr_hour == 0) {
        curr_hour = 12;
    }
    if (curr_hour > 12) {
        curr_hour = curr_hour - 12;
    }
    curr_hour = checkTime(curr_hour);
    curr_minute = checkTime(curr_minute);
    curr_second = checkTime(curr_second);

    var months = ['Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni', 'Juli', 'Agustus', 'September', 'Oktober', 'November', 'Desember'];
    var myDays = ['Minggu', 'Senin', 'Selasa', 'Rabu', 'Kamis', 'Jum&#39;at', 'Sabtu'];
    var date = new Date();
    var day = date.getDate();
    var month = date.getMonth();
    var thisDay = date.getDay(),
        thisDay = myDays[thisDay];
    var yy = date.getYear();
    var year = (yy < 1000) ? yy + 1900 : yy;
    
    document.getElementById('tgl').innerHTML= '<input type=\"text\" value=\"' + thisDay + ', ' + day + ' ' + months[month] + ' ' + year +'\" class=\"form-control text-right\"><div class=\"input-group-append\"><div class=\"input-group-text\"><i class=\"nc-icon nc-calendar-60\"></i></div></div>';
    document.getElementById('clock').innerHTML= '<input type=\"text\" value=\"'+ curr_hour + ":" + curr_minute + ":" + curr_second + " " + a_p +'\" class=\"form-control text-right\"><div class=\"input-group-append\"><div class=\"input-group-text\"><i class=\"fa fa-clock\"></i></div></div>';
    }

